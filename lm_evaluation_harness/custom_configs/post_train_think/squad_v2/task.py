class SQuAD2_Assamese(ConfigurableTask):
    VERSION = 4
    DATASET_PATH = "sarvam/squad_v2"
    DATASET_NAME = None

    def __init__(self, config=None):
        super().__init__(config={"metadata": {"version": self.VERSION}})

    def has_test_docs(self):
        return True

    def test_docs(self):
        return self.dataset["validation"]

    def doc_to_text(self, doc):
        return (
            "Read the passage and answer the question below.\n\n"
            "If the question is answerable from the passage, think step by step and then state your final answer with the phrase: \"the answer is: <answer>\". "
            "Only include the relevant short span as your answer.\n"
            "If the passage does not contain enough information to answer the question, just state \"unanswerable\".\n\n"
            f"Background: {doc['passage_Assamese_translation']}\n\n"
            f"Question: {doc['question_Assamese_translation']}\n\n"
            "Answer:"
        )

    def doc_to_target(self, doc):
        answer_list = doc["answer_list_Assamese_translation"]
        if len(answer_list) > 0:
            answer = answer_list[0]
        else:
            answer = "unanswerable"
        return answer.strip()

    def construct_requests(self, doc, ctx, chat_template=None, apply_chat_template=False, **kwargs):
        return [
            Instance(
                request_type="generate_until",
                doc=doc,
                arguments=(ctx, {"until": ["\n"]}),
                idx=0,
                **kwargs,
            ),
            Instance(
                request_type="loglikelihood",
                doc=doc,
                arguments=(ctx, " unanswerable"),
                idx=0,
                **kwargs,
            ),
        ]

    def process_results(self, doc, results):
        continuation, (logprob_unanswerable, _) = results

        no_answer_probability = exp(logprob_unanswerable)

        import re
        # Extract "the answer is: ..." or "unanswerable"
        match = re.search(
            r"(?:the answer is\s*[:：]?\s*(.*))|(?:\bunanswerable\b)",
            continuation,
            re.IGNORECASE
        )
        if match:
            extracted = match.group(1)
            if extracted is not None:
                extracted = extracted.strip()
            else:
                extracted = "unanswerable"
        else:
            extracted = continuation.strip()

        predictions = {
            "id": doc["id"],
            "prediction_text": extracted,
            "no_answer_probability": no_answer_probability,
        }
        references = {
            "id": doc["id"],
            "answers": {
                'answer_start': doc["answers"]["answer_start"],
                'text': doc["answer_list_Assamese_translation"]
            }
        }

        return {
            "exact": (predictions, references),
            "f1": (predictions, references),
            # "HasAns_exact": (predictions, references),
            # "HasAns_f1": (predictions, references),
            # "NoAns_exact": (predictions, references),
            # "NoAns_f1": (predictions, references),
            # "best_exact": (predictions, references),
            # "best_f1": (predictions, references),
        }

    def aggregation(self):
        return {
            "exact": partial(_squad_agg, "exact"),
            "f1": partial(_squad_agg, "f1"),
            # "HasAns_exact": partial(_squad_agg, "HasAns_exact"),
            # "HasAns_f1": partial(_squad_agg, "HasAns_f1"),
            # "NoAns_exact": partial(_squad_agg, "NoAns_exact"),
            # "NoAns_f1": partial(_squad_agg, "NoAns_f1"),
            # "best_exact": partial(_squad_agg, "best_exact"),
            # "best_f1": partial(_squad_agg, "best_f1"),
        }

    def higher_is_better(self):
        return {
            "exact": True,
            "f1": True,
            # "HasAns_exact": True,
            # "HasAns_f1": True,
            # "NoAns_exact": True,
            # "NoAns_f1": True,
            # "best_exact": True,
            # "best_f1": True,
        }



